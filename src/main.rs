fn main(){
    println!("Welcome, provide a valid PID, or exit using `q`: ");
    let mut input = String::new();
    io::stdin().read_line(&mut input).unwrap();
    let input = input.trim();

    if input.eq_ignore_ascii_case("q") {
        std::process::exit(0);
    }

    let pid: u32 = input.trim().parse().expect("Please enter a valid PID");
    match process_inject(pid) {
        Ok(_) => println!("Success"),
        Err(e) => eprintln!("Err: {}", e),
    }
}

